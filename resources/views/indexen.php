                            <h3 class="strong">Main page</h3></div>
                        <div class="panel-body">
                            <p><strong><u>«Ionosat – Micro»</u></strong> is the Ukrainian satellite mission, realized under the <a
                                    href="https://uk.wikipedia.org/wiki/%D0%9D%D0%B0%D1%86%D1%96%D0%BE%D0%BD%D0%B0%D0%BB%D1%8C%D0%BD%D0%B0_%D0%BA%D0%BE%D1%81%D0%BC%D1%96%D1%87%D0%BD%D0%B0_%D0%BF%D1%80%D0%BE%D0%B3%D1%80%D0%B0%D0%BC%D0%B0_%D0%A3%D0%BA%D1%80%D0%B0%D1%97%D0%BD%D0%B8"
                                    title="Національна космічна програма України">National purpose scientific and technical space program of Ukraine</a>.</p>

                            <p><strong><u>Project task</u></strong> is the study of dynamic processes in the ionosphere, through synchronized space and ground-based measurements to explore the relationship between ionospheric disturbances and processes on the Sun, in the magnetosphere, atmosphere and Earth interior.
                            </p>

                            <p>

                                <strong><u>Patricular project tasks</u></strong>:
                            <ul>
                                <li>Formation of reliable and representative data sets of the ionospheric perturbations along the orbital track depending on helio- and geophysical conditions (solar and magnetic activity, latitude, UT, etc.);
                                </li>
                                <li>Development and testing of the scientific equipment to record electro- and gas-dynamical parameters of the ionosphere in flight conditions;
                                </li>
                                <li>Working of techniques for the ionosphere monitoring by space and ground-based facilities;
                                </li>
                                <li>Verification of technical solutions of the developed the satellite platform;</li>
                                <li>Database and web-resource creation of mission results. The inclusion of collected data into the global geoinformatic systems;
                                </li>
                                <li>Utilization of obtained scientific results in the educational process. Popularization of space research.
                                </li>
                            </ul>
                            </p>
                            <p><strong><u>Methodological principles:</u></strong>
                            <ul>
                                <li>realization aboard one satellite complete diagnostics of ionosphere parameters by contact methods

                                    in a wide range of temporal and spatial scales;
                                </li>
                                <li>priority of monitoring measurements to create

                                    a representative data sample of ionospheric observations;
                                </li>
                                <li>developing of database of disturbed ionosphere parameters in the interests of research in the field of fundamental ionosphere physics, space weather, terragenic

                                    and anthropogenic effects in the near Earth space.
                                </li>
                            </ul>
                            </p>

                            <p>
                                <strong><u>«Ionosat-Micro» scientific instrumentation is intended for registration:</u></strong><br>
                            <ul>
                                <li>global dynamic structure and physical parameters of the neutral atmosphere (exosphere) and ionospheric plasma;
                                </li>
                                <li>detailed spatial structure and physical parameters of the ionospheric current systems and geomagnetic field;
                                </li>
                                <li>physical parameters of the ULF-ELF-VLF electromagnetic wave structures.
                                </li>
                            </ul>
                        </div>
